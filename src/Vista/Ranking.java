    private void actualizarRanking() {
        // Crear un arreglo para almacenar los jugadores activos
        Jugador[] jugadores = new Jugador[loginUser.count];
        int jugadoresActivosCount = 0;

        // Llenar el arreglo con los jugadores activos
        for (int i = 0; i < loginUser.count; i++) {
            if (loginUser.activos[i]) {
                jugadores[jugadoresActivosCount++] = new Jugador(loginUser.usuarios[i], loginUser.puntosArr[i]);
            }
        }

        // Ordenar el arreglo por puntos en orden descendente
        for (int i = 0; i < jugadoresActivosCount - 1; i++) {
            for (int j = i + 1; j < jugadoresActivosCount; j++) {
                if (jugadores[i].getPuntos() < jugadores[j].getPuntos()) {
                    Jugador temp = jugadores[i];
                    jugadores[i] = jugadores[j];
                    jugadores[j] = temp;
                }
            }
        }

        // Construir las cadenas de texto del ranking
        StringBuilder posicionesBuilder = new StringBuilder();
        StringBuilder usernamesBuilder = new StringBuilder();
        StringBuilder puntosBuilder = new StringBuilder();

        for (int i = 0; i < jugadoresActivosCount; i++) {
            Jugador jugador = jugadores[i];
            posicionesBuilder.append(i + 1).append("\n");
            usernamesBuilder.append(jugador.getUsername()).append("\n");
            puntosBuilder.append(jugador.getPuntos()).append("\n");
        }

        // Mostrar el ranking en los JTextArea
        posicionInfo.setText(posicionesBuilder.toString());
        usernameInfo.setText(usernamesBuilder.toString());
        puntosInfo.setText(puntosBuilder.toString());
    }

// Clase Jugador
    private class Jugador {

        private String username;
        private int puntos;

        public Jugador(String username, int puntos) {
            this.username = username;
            this.puntos = puntos;
        }

        public String getUsername() {
            return username;
        }

        public int getPuntos() {
            return puntos;
        }
    }

